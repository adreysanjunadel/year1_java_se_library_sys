    private void loadUserTypes() {
        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `user_type`");

            DefaultComboBoxModel typeModel = (DefaultComboBoxModel) jComboBox1.getModel();
            typeModel.removeAllElements();

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("type"));
                userTypeMap.put(resultSet.getString("type"), resultSet.getString("id"));
            }

            typeModel.addAll(v);
            jComboBox1.setSelectedIndex(0);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadLibrarians(String query) {

        try {
            ResultSet resultSet = MySQL.execute(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                String password = resultSet.getString("password");

                Vector vector = new Vector();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("fname"));
                vector.add(resultSet.getString("lname"));
                vector.add(resultSet.getString("username"));
                vector.add(password); //real pass
                vector.add(resultSet.getString("status"));
                vector.add(resultSet.getString("user_type.type"));
                vector.add(resultSet.getString("mobile"));

                model.addRow(vector);
            }

            int passwordColumnIndex = 4;

            // password column masked renderer
            DefaultTableCellRenderer maskedRenderer = new DefaultTableCellRenderer() {
                @Override
                protected void setValue(Object value) {
                    // asterized if n !null
                    setText(value != null ? "*****" : "");
                }
            };

            // Apply the masked renderer to the password column
            jTable1.getColumnModel().getColumn(passwordColumnIndex).setCellRenderer(maskedRenderer);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading data. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

