    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        // TODO add your handling code here:
        String status = "";
        String id = jTextField4.getText();
        String username = jTextField8.getText();
        String mobile = jTextField9.getText();
        String firstName = jTextField11.getText();
        String lastName = jTextField10.getText();

        if (jRadioButton1.isSelected()) {
            status = "Active";
        } else if (jRadioButton2.isSelected()) {
            status = "Inactive";
        }

        if (username.isEmpty() && mobile.isEmpty() && firstName.isEmpty() && lastName.isEmpty() && status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill at least one search parameter.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            StringBuilder query = new StringBuilder("SELECT * FROM `user` "
                    + "INNER JOIN `status` ON `user`.`status_id` = `status`.`id` "
                    + "INNER JOIN `user_type` ON `user`.`user_type_id` = `user_type`.`id` WHERE 1=1");

            List<Object> parameters = new ArrayList<>();

            if (!id.isEmpty()) {
                query.append(" AND `user`.`id` = ?");
                parameters.add(id);
            }
            if (!username.isEmpty()) {
                query.append(" AND `user`.`username` LIKE ?");
                parameters.add(username + "%"); // Partial match on username
            }
            if (!mobile.isEmpty()) {
                query.append(" AND `user`.`mobile` LIKE ?");
                parameters.add(mobile + "%"); // Partial match on mobile
            }
            if (!firstName.isEmpty()) {
                query.append(" AND `user`.`fname` LIKE ?");
                parameters.add(firstName + "%"); // Partial match on first name
            }
            if (!lastName.isEmpty()) {
                query.append(" AND `user`.`lname` LIKE ?");
                parameters.add(lastName + "%"); // Partial match on last name
            }
            if (!status.isEmpty()) {
                query.append(" AND `status`.`status` = ?");
                parameters.add(status);
            }

            try (PreparedStatement pstmt = MySQL.prepareStatement(query.toString())) {
                for (int i = 0; i < parameters.size(); i++) {
                    pstmt.setObject(i + 1, parameters.get(i));
                }

                ResultSet resultSet = pstmt.executeQuery();

                if (!resultSet.isBeforeFirst()) {
                    JOptionPane.showMessageDialog(this, "Cannot Find Match", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.setRowCount(0);

                    while (resultSet.next()) {
                        String password = resultSet.getString("password");

                        Vector<String> vector = new Vector<>();
                        vector.add(resultSet.getString("id"));
                        vector.add(resultSet.getString("fname"));
                        vector.add(resultSet.getString("lname"));
                        vector.add(resultSet.getString("username"));
                        vector.add(password); // Real password (will be masked in display)
                        vector.add(resultSet.getString("status"));
                        vector.add(resultSet.getString("user_type.type"));
                        vector.add(resultSet.getString("mobile"));

                        model.addRow(vector);
                    }

                    int passwordColumnIndex = 4;

                    // Password column masked renderer
                    DefaultTableCellRenderer maskedRenderer = new DefaultTableCellRenderer() {
                        @Override
                        protected void setValue(Object value) {
                            setText(value != null ? "*****" : "");
                        }
                    };

                    // Apply the masked renderer to the password column
                    jTable1.getColumnModel().getColumn(passwordColumnIndex).setCellRenderer(maskedRenderer);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton8ActionPerformed

