    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Search Reservation
        String status = "";
        String cardId = jTextField5.getText();
        String fname = jTextField9.getText();
        String lname = jTextField10.getText();
        String bookId = jTextField11.getText();
        String bookName = jTextField12.getText();

        if (jRadioButton3.isSelected()) {
            status = "Active";
        } else if (jRadioButton4.isSelected()) {
            status = "Inactive";
        }

        if (cardId.isEmpty() && fname.isEmpty() && lname.isEmpty() && bookId.isEmpty() && bookName.isEmpty() && status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill at least one search parameter.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            StringBuilder searchQuery = new StringBuilder(query);

            List<Object> parameters = new ArrayList<>();

            if (!cardId.isEmpty()) {
                searchQuery.append(" AND `reservation`.`membercard_id` = ?");
                parameters.add(cardId);
            }
            if (!fname.isEmpty()) {
                searchQuery.append(" AND `member`.`fname` LIKE ?");
                parameters.add(fname + "%");
            }
            if (!lname.isEmpty()) {
                searchQuery.append(" AND `member`.`lname` LIKE ?");
                parameters.add(lname + "%");
            }
            if (!bookId.isEmpty()) {
                searchQuery.append(" AND `reservation`.`book_id` LIKE ?");
                parameters.add(bookId + "%");
            }
            if (!bookName.isEmpty()) {
                searchQuery.append(" AND `book`.`name` LIKE ?");
                parameters.add(bookName + "%"); // Partial match on last name
            }
            if (!status.isEmpty()) {
                searchQuery.append(" AND `status`.`status` = ?");
                parameters.add(status);
            }

            try (PreparedStatement pstmt = MySQL.prepareStatement(searchQuery.toString())) {
                for (int i = 0; i < parameters.size(); i++) {
                    pstmt.setObject(i + 1, parameters.get(i));
                }

                ResultSet resultSet = pstmt.executeQuery();

                if (!resultSet.isBeforeFirst()) {
                    JOptionPane.showMessageDialog(this, "Cannot Find Match", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.setRowCount(0);

                    while (resultSet.next()) {
                        Vector vector = new Vector();
                        vector.add(resultSet.getString("reservation.id"));
                        vector.add(resultSet.getString("reservation.membercard_id"));
                        vector.add(resultSet.getString("reservation.book_id"));
                        vector.add(resultSet.getString("book.name"));
                        vector.add(resultSet.getString("status.status"));
                        vector.add(resultSet.getString("reservation.date_time"));

                        model.addRow(vector);
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {

            int selectedRow = jTable1.getSelectedRow();

            String cardId = String.valueOf(jTable1.getValueAt(selectedRow, 1));
            String bookId = String.valueOf(jTable1.getValueAt(selectedRow, 2));
            String bookName = String.valueOf(jTable1.getValueAt(selectedRow, 3));
            String status = String.valueOf(jTable1.getValueAt(selectedRow, 4));

            try {
                // Check if mobile number or username already exists
                String checkQuery = "SELECT * FROM `reservation` "
                        + "INNER JOIN `member` ON `reservation`.`membercard_id` = `member`.`membercard_id` "
                        + "WHERE `reservation`.`status_id` = ? OR `reservation`.`book_id` = ?";
                PreparedStatement checkStmt = MySQL.prepareStatement(checkQuery);
                checkStmt.setInt(1, 1);
                checkStmt.setString(2, bookId);
                ResultSet resultSet = checkStmt.executeQuery();

                if (resultSet.next()) {
                    String fname = resultSet.getString("member.fname");
                    String lname = resultSet.getString("member.lname");

                    jTextField7.setText(fname);
                    jTextField8.setText(lname);

                } else {
                    JOptionPane.showMessageDialog(this, "Cannot Find Match", "Warning", JOptionPane.WARNING_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            jTextField3.setText(cardId);
            jTextField4.setText(bookId);
            jTextField6.setText(bookName);

            if ("Active".equals(status)) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }

            jRadioButton1.setEnabled(true);
            jRadioButton2.setEnabled(true);

            //Cannot add same details
            jButton2.setEnabled(false);

        }
    }//GEN-LAST:event_jTable1MouseClicked

