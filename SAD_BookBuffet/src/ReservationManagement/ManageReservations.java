    private void loadReservations(String query) {

        try {
            ResultSet resultSet = MySQL.execute(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("reservation.id"));
                vector.add(resultSet.getString("reservation.membercard_id"));
                vector.add(resultSet.getString("reservation.book_id"));
                vector.add(resultSet.getString("book.name"));
                vector.add(resultSet.getString("status.status"));
                vector.add(resultSet.getString("reservation.date_time"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading data. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

