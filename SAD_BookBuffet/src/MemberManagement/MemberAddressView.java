    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        //INSERT
        String no = jTextField1.getText();
        String line1 = jTextField2.getText();
        String line2 = jTextField3.getText();
        String city = jTextField4.getText();

        if (no.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address No", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address Line 1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address Line 2", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address City", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            boolean isFound = false;

            for (int i = 0; i < jTable2.getRowCount(); i++) {

                String getno = String.valueOf(jTable2.getValueAt(i, 1));
                String getLine1 = String.valueOf(jTable2.getValueAt(i, 2));
                String getLine2 = String.valueOf(jTable2.getValueAt(i, 3));
                String getCity = String.valueOf(jTable2.getValueAt(i, 4));

                if (no.equals(getno) && line1.equals(getLine1) && line2.equals(getLine2) && city.equals(getCity)) {
                    JOptionPane.showMessageDialog(this, "Address Already Added", "Warning", JOptionPane.WARNING_MESSAGE);
                    isFound = true;
                    break;
                }

            }

            if (!isFound) {

                try {

                    MySQL.execute("INSERT INTO `member_address`(`member_membership_id`,`no`,`line1`,`line2`,`city`)"
                            + "VALUES('" + this.membercard_id + "','" + no + "','" + line1 + "','" + line2 + "','" + city + "')");

                    loadAddress();
                    reset();

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        //REMOVE
        try {

            int row = jTable2.getSelectedRow();

            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Please Select the Address to Remove", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String id = String.valueOf(jTable2.getValueAt(row, 0));

                MySQL.execute("DELETE FROM `member_address` WHERE `id`='" + id + "'");

                loadAddress();
                reset();

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

